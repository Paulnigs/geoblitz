export function getCountriesByDifficulty(difficulty: "easy" | "medium" | "hard"): Country[] {
  if (difficulty === "easy") {
    return countries.filter((c) => c.difficulty === "easy")
  }
  if (difficulty === "medium") {
    return countries.filter((c) => c.difficulty === "easy" || c.difficulty === "medium")
  }
  return countries
}

export function getRandomCountries(
  count: number,
  exclude?: string,
  difficulty?: "easy" | "medium" | "hard",
): Country[] {
  const pool = difficulty ? getCountriesByDifficulty(difficulty) : countries
  const filtered = exclude ? pool.filter((c) => c.name !== exclude) : pool
  const shuffled = [...filtered].sort(() => Math.random() - 0.5)
  return shuffled.slice(0, count)
}

export function getRandomCountry(difficulty?: "easy" | "medium" | "hard"): Country {
  const pool = difficulty ? getCountriesByDifficulty(difficulty) : countries
  return pool[Math.floor(Math.random() * pool.length)]
}

// Daily challenge - generates consistent question for the day
export function getDailyChallenge(): { country: Country; type: "flag" | "capital" | "outline" } {
  const today = new Date()
  const seed = today.getFullYear() * 10000 + (today.getMonth() + 1) * 100 + today.getDate()
  const countryIndex = seed % countries.length
  const types: ("flag" | "capital" | "outline")[] = ["flag", "capital", "outline"]
  const typeIndex = seed % 3

  return {
    country: countries[countryIndex],
    type: types[typeIndex],
  }
}
